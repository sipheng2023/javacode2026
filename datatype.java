public class datatype {

    public static void main(String[] args) {
        int a = 10;
        float b = 3.14f;
        double c = 3.14159;
        char d = 'A';
        boolean e = true;

        System.out.println("Integer: " + a); // Output: Integer: 10
        System.out.println("Float: " + b); // Output: Float: 3.14
        System.out.println("Double: " + c); // Output: Double: 3.14159
        System.out.println("Character: " + d); // Output: Character: A
        System.out.println("Boolean: " + e); // Output: Boolean: true
    }

}